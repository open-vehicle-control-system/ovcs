defmodule Ros2.SensorMsgs.Msg.ImuTest do
  use ExUnit.Case, async: true

  alias Ros2.BuiltinInterfaces.Msg.Time
  alias Ros2.GeometryMsgs.Msg.Quaternion
  alias Ros2.GeometryMsgs.Msg.Vector3
  alias Ros2.RmwZenoh
  alias Ros2.SensorMsgs.Msg.Imu
  alias Ros2.StdMsgs.Msg.Header

  # Build a fully-populated Imu with non-trivial values so the
  # encoder/parser can't accidentally pass by zeroing everything.
  defp sample do
    %Imu{
      header: %Header{
        stamp: %Time{sec: 1_700_000_000, nanosec: 123_456_789},
        frame_id: "imu_link"
      },
      orientation: %Quaternion{x: 0.1, y: 0.2, z: 0.3, w: :math.sqrt(1.0 - 0.14)},
      orientation_covariance: List.duplicate(0.0, 9),
      angular_velocity: %Vector3{x: 0.4, y: 0.5, z: 0.6},
      angular_velocity_covariance: List.duplicate(0.0, 9),
      linear_acceleration: %Vector3{x: 0.7, y: 0.8, z: 9.81},
      linear_acceleration_covariance: List.duplicate(0.0, 9)
    }
  end

  test "round-trips through encode → decode_payload → parse" do
    message = sample()

    # Mirrors the publish path: ZenohClient calls
    # RmwZenoh.encode_payload(message) before put; subscribers strip
    # the encapsulation header via decode_payload before parse.
    wire = RmwZenoh.encode_payload(message)
    {:ok, body} = RmwZenoh.decode_payload(wire)
    {:ok, parsed, rest} = Imu.parse(body)

    assert rest == ""
    assert parsed.header.stamp.sec == message.header.stamp.sec
    assert parsed.header.stamp.nanosec == message.header.stamp.nanosec
    assert parsed.header.frame_id == message.header.frame_id

    assert_in_delta parsed.orientation.x, message.orientation.x, 1.0e-12
    assert_in_delta parsed.orientation.y, message.orientation.y, 1.0e-12
    assert_in_delta parsed.orientation.z, message.orientation.z, 1.0e-12
    assert_in_delta parsed.orientation.w, message.orientation.w, 1.0e-12

    assert_in_delta parsed.angular_velocity.x, message.angular_velocity.x, 1.0e-12
    assert_in_delta parsed.linear_acceleration.z, message.linear_acceleration.z, 1.0e-12

    assert length(parsed.orientation_covariance) == 9
    assert Enum.all?(parsed.orientation_covariance, &(&1 == 0.0))
  end

